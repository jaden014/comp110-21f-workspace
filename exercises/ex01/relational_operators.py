"""Examples of relational operators in python."""
__author__ = "730402799"
left_side: int = int(input("Left_integer?"))
right_side: int = int(input("Right_integer?"))
print(str(left_side) + " < " + str(right_side) + " is " + str(left_side<right_side))
print(str(left_side) + " >= " + str(right_side) + " is " + str(left_side>=right_side))
print(str(left_side) + " == " + str(right_side) + " is " + str(left_side==right_side))
print(str(left_side) + " != " + str(right_side) + " is " + str(left_side!=right_side))